def run(args):
    if not args:
        return "Usage: code <task> [language]\nExample: code fibonacci python"
    
    import sys
    from pathlib import Path
    
    # Add paths
    agent_dir = Path(__file__).parent.parent
    sys.path.insert(0, str(agent_dir))
    
    # Import from engine (not core)
    from engine import ProgrammingEngine
    
    parts = args.split()
    task = parts[0]
    language = parts[1] if len(parts) > 1 else "python"
    
    engine = ProgrammingEngine(language)
    result = engine.generate_code(task)
    
    output = f"💻 {language.upper()} CODE: {task}\n\n{result['code']}"
    output += f"\n\n✨ Generated by: {result['model']}"
    return output
